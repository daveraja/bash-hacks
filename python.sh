#----------------------------------------------------------------------
# Return the default version of python 
#----------------------------------------------------------------------
python_default_base_version () {
    echo $(python -V 2>&1 | sed -e s'/Python\s*\([0-9]*\.[0-9]*\)\..*$/\1/')
}
